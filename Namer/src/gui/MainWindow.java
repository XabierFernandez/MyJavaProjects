    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            // TODO add your handling code here:
            this.aFileWriter = new BufferedWriter(new FileWriter(this.aFile));
        } catch (IOException ex) {
            Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        if ((jTextField1.getText().equals("")) || (jTextField2.getText().equals("")) || (jTextField3.getText().equals("")) || (jList2.getModel().getSize()==0)){
            Toolkit.getDefaultToolkit().beep();
            OUDialog.alert("Existen campos vacios");
        } else {
            this.jButton6.setVisible(false);
            if( OUDialog.confirm("¿Desea Proceder?")){
                this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));      
                List <String> listaModulos;
                listaModulos = new ArrayList<>();
                String excelPath=jTextField3.getText();
                String dirMod=jTextField2.getText();
                String initDat =  jComboBox1.getSelectedItem().toString();
                String finalDat =  jComboBox7.getSelectedItem().toString();
                String fieldVS20 = jComboBox2.getSelectedItem().toString();
                String fieldNCV2 = jComboBox3.getSelectedItem().toString();
            
                String fieldX =  jComboBox4.getSelectedItem().toString();
                String fieldY =  jComboBox5.getSelectedItem().toString();
                String fieldZ =  jComboBox6.getSelectedItem().toString();
                
                try {
           
                    this.aFileWriter.write(this.jTextField3.getText());
            
                } catch (IOException ex) {
                    Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                }
                finally{
                    try {
                   
                        this.aFileWriter.close(); 
                    }   
                    catch (IOException ex) {
                        OUDialog.alert("ERROR: Problema con acceso al sistema de archivos\n" + ex);
                    }
                }
            
        
                for(int i=0;i<jList2.getModel().getSize();i++){
                    listaModulos.add(jTextField1.getText()+"\\" + jList2.getModel().getElementAt(i).toString());
            
                }      
      
               
                    
                this.namecoord = new NameCoord(listaModulos, excelPath, dirMod, initDat,finalDat, fieldVS20, fieldNCV2, fieldX, fieldY, fieldZ);
                
                this.namecoord.proceder();
                this.namecoord.writeLogFileClose();
                    
                
                try {
                    SoundUtils.tone(1000,100);
                    //Thread.sleep(1000);
                    SoundUtils.tone(100,100);
                    //Thread.sleep(200);
                    SoundUtils.tone(5000,100);
                    //Thread.sleep(200);
                    SoundUtils.tone(400,100);
                    //Thread.sleep(200);
                    SoundUtils.tone(400,100, 0.3);
                } catch (LineUnavailableException ex) {
                    Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                }
                /*
                catch (InterruptedException ex) {
                    Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                }*/
                this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                OUDialog.alert("Proceso Finalizado");
                this.jButton6.setVisible(true);
           
            
          
            }
            else{
                this.jButton6.setVisible(true);
                jList2.setListData(new Object[0]);
                jList1.setListData(new Object[0]);
                jTextField1.setText("");
                jTextField2.setText("");
                
            
        
        }
    }
        
        
    
    }//GEN-LAST:event_jButton6ActionPerformed

