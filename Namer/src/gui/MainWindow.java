    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String aPathName;
        File dir;
        File [] filesInDir;
        String [] strings;
        strings = new String [0];
        boolean bOk = false;
       
        try{
                    
             fs.showDialog(this, null);
             dir = fs.getSelectedFile();
             aPathName=dir.getAbsolutePath();
             
             
             filesInDir = dir.listFiles();
             String [] names = new String[filesInDir.length];
             
             for(int i=0;i<filesInDir.length;i++){
                 
                 if ((filesInDir[i].getName().endsWith(".mod"))||(filesInDir[i].getName().endsWith(".sys"))){
                     
                     bOk = true;
                     names[i]=filesInDir[i].getName();
                     jList1.setListData(names);
                     jList1.setSelectionBackground(Color.green);
                     jList1.setSelectionForeground(Color.black);
                     jTextField1.setText(aPathName);   
                     if((filesInDir[i].getName().contains("M_DatosGlobalPrg.mod"))||((filesInDir[i].getName().contains("M_V"))&& (filesInDir[i].getName().contains("R.mod"))) ){
                         
                         items.add(filesInDir[i].getName());
                         jList2.setListData(items.toArray());
                     }
                 }
                 
             }
             
             if(bOk == false){
                 jList1.setListData(strings);
                 Toolkit.getDefaultToolkit().beep();
                 OUDialog.alert("AVISO: El directorio no contiene ningun\n "
                         + "archivo ABB Rapid ");
             }
             
             
              
        }
        catch(NullPointerException e){
             Toolkit.getDefaultToolkit().beep();
             OUDialog.alert("ERROR: No se a selecionado ningun directorio\n " + e);
        }
       
       
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

